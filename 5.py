# Compute a) sin of 60 degree b) cos of pi c) sin(0.8660254037844386)
# d) tan of 90 degree.
import math
print('sin of 60 degree = {}'.format(math.sin(60)))
print('cos of pi = {}'.format(math.cos(math.pi)))
print('sin(0.8660254037844386) = {}'.format(math.sin(0.8660254037844386)))
print('tan of 90 degree = {}'.format(math.tan(90)))
