# Compute a) 5 to the power of 8 b) square root of 400 c) exponent of 5
# d) Logarithm of 625 base 5.

import math
print('5 to the power of 8 = {}'.format(math.pow(5, 8)))
print('Square root of 400 = {}'.format(math.sqrt(400)))
print('Exponent of 5 and 3 = {}'.format(math.pow(5, 3)))
print('Logarithm of 625 base 5 = {}'.format(math.log(625,5)))
